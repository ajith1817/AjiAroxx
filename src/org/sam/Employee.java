package org.sam;

public class Employee{


private int idno;
private String Name;
private long Mobile ;
public int getIdno() {
	return idno;
}
public void setIdno(int idno) {
	this.idno = idno;
}
public String getName() {
	return Name;
}
public void setName(String name) {
	Name = name;
}
public long getMobile() {
	return Mobile;
}
public void setMobile(long mobile) {
	Mobile = mobile;
}




}
